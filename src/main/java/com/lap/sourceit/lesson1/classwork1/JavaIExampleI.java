package com.lap.sourceit.lesson1.classwork1;

public class JavaIExampleI {

    /**
     * This program calculates the sum and difference of two numbers
     */
    public static void main(String[] args) {

        //init variables
        int a = 25;
        int b = 50;

        //calculates the sum and difference
        int sum = a + b;
        int diff = b - a;

        //printing result
        System.out.println("sum: " + sum);
        System.out.println("diff: " + diff);
    }

}
